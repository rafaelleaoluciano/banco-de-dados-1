    NUMPARCELA INT NOT NULL,
    VALORPARCELA DECIMAL(15,2) NOT NULL,
    DATAVENCIMENTO DATE NOT NULL,
    PRIMARY KEY (IDPARCELA),
    CONSTRAINT FK_PARCELA_EMPRESTIMO FOREIGN KEY (IDEMPRESTIMO) REFERENCES SYBANK.EMPRESTIMO (IDEMPRESTIMO)
);

