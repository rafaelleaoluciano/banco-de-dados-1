-- TIPO DE CONTA
CREATE TABLE IF NOT EXISTS SYBANK.TIPOCONTA
(
	IDTIPOCONTA INT NOT NULL,
    DESCTIPOCONTA VARCHAR(50) NOT NULL,
    PRIMARY KEY (IDTIPOCONTA)
);

-- CONTA
CREATE TABLE IF NOT EXISTS SYBANK.CONTA
(
	IDCONTA INT NOT NULL,
    IDTIPOCONTA INT NOT NULL,
    NUMCONTA VARCHAR(20) NOT NULL,
    NUMAGENCIA VARCHAR(10) NOT NULL,
    SALDO DECIMAL(15,2) NOT NULL,
    PRIMARY KEY (IDCONTA),
    CONSTRAINT FK_TIPOCONTA_CONTA FOREIGN KEY (IDTIPOCONTA) REFERENCES SYBANK.TIPOCONTA (IDTIPOCONTA)
);

-- RELAÇÃO PESSOA E CONTA
CREATE TABLE IF NOT EXISTS SYBANK.PESSOA_CONTA
(
	IDPESSOA INT NOT NULL,
    IDCONTA INT NOT NULL,
    PRIMARY KEY (IDPESSOA),
    CONSTRAINT FK_PESSOA_CONTA_PESSOA FOREIGN KEY (IDCONTA) REFERENCES SYBANK.CONTA (IDCONTA)
);

-- TIPO DE TRANSAÇÃO
CREATE TABLE IF NOT EXISTS SYBANK.TIPOTRANSACAO
(
	IDTIPOTRANSACAO INT NOT NULL,
    DESCTRANSACAO VARCHAR(50) NOT NULL,
    PRIMARY KEY (IDTIPOTRANSACAO)
);

-- TRANSAÇÃO 
CREATE TABLE IF NOT EXISTS SYBANK.TRANSACAO
(
	IDTRANSACAO INT NOT NULL,
    IDTIPOTRANSACAO INT NOT NULL,
    IDCONTAORIGEM INT NOT NULL,
    IDCONTADESTINO INT NOT NULL,
    VALOR DECIMAL (15,2) NOT NULL,
    DATATRANSACAO DATE NOT NULL,
    DESCRICAO VARCHAR (100),
    PRIMARY KEY (IDTRANSACAO),
    CONSTRAINT FK_TRANSACAO_TIPO FOREIGN KEY (IDTIPOTRANSACAO) REFERENCES SYBANK.TIPOTRANSACAO (IDTIPOTRANSACAO),
    CONSTRAINT FK_TRANSACAO_CONTAORIGEM FOREIGN KEY (IDCONTAORIGEM) REFERENCES SYBANK.CONTA (IDCONTA),
    CONSTRAINT FK_TRANSACAO_CONTADESTINO FOREIGN KEY (IDCONTADESTINO) REFERENCES SYBANK.CONTA (IDCONTA)
);

-- EXTRATO
CREATE TABLE IF NOT EXISTS SYBANK.EXTRATO
(
	IDEXTRATO INT NOT NULL,
    IDCONTA INT NOT NULL,
    IDTRANSACAO INT NOT NULL,
    SALDOANTERIOR DECIMAL(15,2),
    SALDOATUAL DECIMAL(15,2),
    PRIMARY KEY (IDEXTRATO),
    CONSTRAINT FK_EXTRATO_CONTA FOREIGN KEY (IDCONTA) REFERENCES SYBANK.CONTA (IDCONTA),
    CONSTRAINT FK_EXTRATO_TRANSACAO FOREIGN KEY (IDTRANSACAO) REFERENCES SYBANK.TRANSACAO (IDTRANSACAO)
);

-- CARTÃO
CREATE TABLE IF NOT EXISTS SYBANK.CARTAO
(
	IDCARTAO INT NOT NULL,
    IDCONTA INT NOT NULL,
    NUMCARTAO VARCHAR(20) NOT NULL,
    TIPOCARTAO VARCHAR(20) NOT NULL,
    LIMITE_TOTAL DECIMAL(15,2),
    LIMITE_DISPONIVEL DECIMAL(15,2),
    PRIMARY KEY (IDCARTAO),
    CONSTRAINT FK_CARTAO_CONTA FOREIGN KEY (IDCONTA) REFERENCES SYBANK.CONTA (IDCONTA)
);

-- FATURA 
CREATE TABLE IF NOT EXISTS SYBANK.FATURA
(
	IDFATURA INT NOT NULL,
    IDCARTAO INT NOT NULL,
    VALORFATURA DECIMAL(15,2),
    DATAVENCIMENTO DATE,
    PRIMARY KEY (IDFATURA),
    CONSTRAINT FK_FATURA_CARTÃO FOREIGN KEY (IDCARTAO) REFERENCES SYBANK.CARTAO (IDCARTAO)
);

-- EMPRÉSTIMO
CREATE TABLE IF NOT EXISTS SYBANK.EMPRESTIMO
(
	IDEMPRESTIMO INT NOT NULL,
    IDPESSOA INT NOT NULL,
    IDCONTA INT NOT NULL,
    VALOR DECIMAL(15,2) NOT NULL,
    JUROS DECIMAL(5,2) NOT NULL,
    QTD_PARCELAS INT NOT NULL,
    PRIMARY KEY (IDEMPRESTIMO),
    CONSTRAINT FK_EMPRESTIMO_PESSOA FOREIGN KEY (IDPESSOA) REFERENCES SYBANK.PESSOA (IDPESSOA),
    CONSTRAINT FK_EMPRESTIMO_CONTA FOREIGN KEY (IDCONTA) REFERENCES SYBANK.CONTA (IDCONTA)
);

-- PARCELA
CREATE TABLE IF NOT EXISTS SYBANK.PARCELA
(
	IDPARCELA INT NOT NULL,
