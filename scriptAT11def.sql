CREATE TABLE IF NOT EXISTS PESSOA
(
	IDPESSOA INT NOT NULL,
    NOMEPESSOA VARCHAR(100) NOT NULL,
    NUMTELEFONE VARCHAR(11) NOT NULL,
    EMAILPESSOA VARCHAR (100) NOT NULL,
    PRIMARY KEY (IDPESSOA)
);

